package operators;

public class ArithmeticOperation1 {

	public static void main(String arg[])
	{
		int x = 10;
		int y = 20;
		
		System.out.println(x+y);
		System.out.println(y/x);
		
		System.out.println(y%x);
		
		// Modulus operator for remainder
		if(y%x==0) {
			System.out.println("Completely divisible");
		}
		
		
		
		
	}
	
}
