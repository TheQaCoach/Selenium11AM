package operators;

public class ArithmeticOperation3 {

	public static void main(String arg[])
	{
		int x = 10;
		int y = 10;
		
		System.out.println("Value of x : "+ (x++)); // Post increment
		
		System.out.println("Value of x : "+ (x));
		
		System.out.println("Value of y : "+ (++y)); // Pre increment
		
	}
}
