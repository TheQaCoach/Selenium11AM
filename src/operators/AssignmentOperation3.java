package operators;

public class AssignmentOperation3 {

	public static void main(String arg[])
	{
		int x = 0;
		int y = 0;
		int z = 5;
		
		x+=1;   // x = x+1
		y-=1;   // y = y-1
		z%=2;   // z = z%2
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		
		
		
	}
}
